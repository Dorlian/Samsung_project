from __future__ import annotations

from pathlib import Path

from docx import Document
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.shared import Pt


def main() -> None:
    root = Path(__file__).resolve().parents[1]
    out = root / "Пояснительная_записка_Фотоассистент.docx"

    doc = Document()
    style = doc.styles["Normal"]
    style.font.name = "Times New Roman"
    style.font.size = Pt(14)

    def add_center(lines: list[str]) -> None:
        for t in lines:
            p = doc.add_paragraph(t.strip())
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER

    def add_left(text: str) -> None:
        doc.add_paragraph(text)

    def add_heading(num: str, title: str) -> None:
        p = doc.add_paragraph()
        r = p.add_run(f"{num}. {title}")
        r.bold = True

    add_center(
        [
            "МИНИСТЕРСТВО НАУКИ И ВЫСШЕГО ОБРАЗОВАНИЯ РОССИЙСКОЙ ФЕДЕРАЦИИ",
            "ФЕДЕРАЛЬНОЕ ГОСУДАРСТВЕННОЕ БЮДЖЕТНОЕ ОБРАЗОВАТЕЛЬНОЕ УЧРЕЖДЕНИЕ ВЫСШЕГО ОБРАЗОВАНИЯ",
            "«МАГНИТОГОРСКИЙ ГОСУДАРСТВЕННЫЙ ТЕХНИЧЕСКИЙ",
            "УНИВЕРСИТЕТ ИМ. Г.И. НОСОВА»",
            "(ФГБОУ ВО «МГТУ ИМ. Г.И.НОСОВА»)",
            "Институт энергетики и автоматизированных систем",
            "Кафедра бизнес – информатики и информационных технологий",
        ]
    )
    doc.add_paragraph()
    p = doc.add_paragraph()
    r = p.add_run("ПРОЕКТНОЕ ЗАДАНИЕ")
    r.bold = True
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER

    add_center(
        [
            "по дисциплине: Компьютерное зрение и интеллектуальные системы",
            "на тему: Разработка десктоп-приложения «Фотоассистент фотографа» для автоматической сортировки "
            "фотографий по экспозиции, резкости и признаку закрытых глаз",
            "Исполнитель: _________________________, студент ___ курса, группа ___________",
            "Руководитель: _________________________, _________________________________",
            'Работа допущена к защите «___» _____________ 20__ г. __________',
            "(подпись)",
            'Работа защищена «___» _____________ 20__ г. с оценкой __________     __________',
            "    (оценка)         (подпись)",
            "Магнитогорск, 2026 г.",
        ]
    )

    doc.add_paragraph()
    add_left("Содержание")
    for line in (
        "Введение",
        "Описание структуры проекта",
        "Инструкция по установке и запуску",
        "Описание датасета",
        "Архитектура моделей и обучение",
        "Интеграция и пользовательский интерфейс",
        "Заключение",
        "Список источников",
    ):
        doc.add_paragraph(line)

    # --- 1 Введение ---
    add_heading("1", "Введение")
    intro = (
        "Профессиональная и любительская фотосъёмка сопровождается съёмкой большого числа кадров, "
        "в том числе с техническими дефектами: неудачная экспозиция (пересвет или недосвет), смаз, "
        "а также портреты с закрытыми глазами. Ручной отбор удачных снимков отнимает много времени."
        "\n\n"
        "Современные средства компьютерного зрения и машинного обучения позволяют автоматизировать "
        "предварительный отбор кадров по формализуемым критериям: анализ гистограммы яркости, оценка "
        "резкости по отклику оператора Лапласа, детекция лица и оценка состояния век с помощью "
        "модели лицевых ориентиров и при необходимости — дополнительной свёрточной нейросети."
        "\n\n"
        "Целью проекта является разработка десктоп-приложения на языке Python с графическим интерфейсом, "
        "которое рекурсивно обрабатывает изображения в выбранной папке и распределяет файлы в каталоги "
        "«Удачные» и «Неудачные» с указанием причины отбраковки."
        "\n\n"
        "Основные задачи: анализ предметной области и выбор метрик; подготовка и использование датасета "
        "MRL Eye Dataset для обучения классификатора «открытый / закрытый глаз»; обучение модели ResNet-18; "
        "интеграция детекции лиц MediaPipe Face Landmarker; разработка пользовательского интерфейса и "
        "тестирование приложения."
    )
    doc.add_paragraph(intro)

    # --- 2 Структура ---
    add_heading("2", "Описание структуры проекта")
    struct = (
        "Репозиторий проекта размещён на GitHub: https://github.com/Dorlian/Samsung_project\n\n"
        "Корневые элементы:\n"
        "— app/__main__.py — точка входа (python -m app), Tkinter, пакетная обработка в фоне;\n"
        "— requirements/requirements.txt — зависимости (OpenCV, PyTorch, MediaPipe и др.);\n"
        "— run.bat, bin/run.sh — быстрый запуск под Windows и Unix;\n"
        "— packaging/photo_assistant.spec, build_exe.bat — сборка EXE (PyInstaller);\n"
        "— models/ — каталог для face_landmarker.task (загрузка при первом запуске) и файла весов "
        "eye_state_resnet18.pth (обученный классификатор глаз, может поставляться вместе с репозиторием);\n"
        "— training/ — инструкции и плейсхолдеры для данных обучения.\n\n"
        "Исходный код:\n"
        "— src/analysis/ — модули экспозиции, резкости, анализа глаз (MediaPipe + опционально CNN), "
        "сборка пайплайна PhotoAnalyzer;\n"
        "— src/config/settings.py — загрузка и сохранение порогов в app_settings.json;\n"
        "— src/models/eye_classifier.py — архитектура EyeStateModel (ResNet-18, два класса);\n"
        "— src/training/prepare_mrl_dataset.py и train_eye_classifier.py — подготовка датасета MRL и обучение;\n"
        "— src/ui/gallery.py — раздел «Альбомы», просмотр и ручной перенос снимков;\n"
        "— src/utils/file_ops.py — обход файлов, перемещение в папки результатов."
    )
    doc.add_paragraph(struct)

    # --- 3 Установка ---
    add_heading("3", "Инструкция по установке и запуску")
    install = (
        "Для работы необходим интерпретатор Python версии 3.10, 3.11 или 3.12 (для стабильной работы "
        "пакета mediapipe не рекомендуется использовать Python 3.13 и новее без проверки совместимости)."
        "\n\n"
        "Последовательность действий:\n"
        "1) Клонировать репозиторий или распаковать архив проекта.\n"
        "2) Создать виртуальное окружение: python -m venv .venv\n"
        "3) Активировать окружение и выполнить: pip install -r requirements/requirements.txt\n"
        "4) Запуск: python -m app (либо run.bat в Windows, ./bin/run.sh в Linux/macOS после chmod +x)."
        "\n\n"
        "При первом анализе изображений с лицами приложение загружает модель MediaPipe Face Landmarker "
        "(файл face_landmarker.task) в каталог models/; требуется подключение к сети Интернет. "
        "Файл весов eye_state_resnet18.pth при наличии в models/ подключается для дополнительной проверки "
        "закрытых глаз (настраивается в разделе «Настройки» приложения)."
        "\n\n"
        "Подробные шаги и типичные ошибки описаны в файле README.md репозитория."
    )
    doc.add_paragraph(install)

    # --- 4 Датасет ---
    add_heading("4", "Описание датасета")
    dataset = (
        "Для обучения классификатора состояния глаз использован открытый датасет MRL Eye Dataset "
        "(изображения областей глаз с метками «открыт» / «закрыт»). Официальный источник: "
        "https://mrl.cs.vsb.cz/eyedataset (при необходимости доступны зеркала на ресурсах Kaggle)."
        "\n\n"
        "Скрипт prepare_mrl_dataset.py преобразует исходную структуру к виду с каталогами train/open, "
        "train/closed, val/open, val/closed. Доля валидации задаётся параметром (например, 0.2)."
        "\n\n"
        "Полный объём исходного датасета в репозиторий проекта не включён из‑за размера; инструкции по "
        "скачиванию и подготовке приведены в каталоге training/."
    )
    doc.add_paragraph(dataset)

    # --- 5 Модели ---
    add_heading("5", "Архитектура моделей и обучение")
    arch = (
        "Детекция лица и оценка «моргания» выполняется средствами MediaPipe Face Landmarker с выходом "
        "blendshape-категорий eyeBlink_L и eyeBlink_R (значения от 0 до 1). По порогам на blendshapes "
        "принимается решение о закрытых глазах; при наличии файла весов дополнительно используются "
        "кропы областей глаз и свёрточная сеть."
        "\n\n"
        "Классификатор глаз реализован как модификация ResNet-18: последний полносвязный слой заменён "
        "на линейный слой с двумя выходами (классы «закрыт» и «открыт»). Обучение выполняется с нуля "
        "(без загрузки предобученных весов ImageNet в скрипте по умолчанию). Параметры обучения по умолчанию: "
        "размер входного кропа 64×64, пакет (batch) 64, число эпох 10, оптимизатор Adam с learning rate 1e-3, "
        "функция потерь — перекрёстная энтропия. Лучшие веса по метрике accuracy на валидации сохраняются "
        "в файл models/eye_state_resnet18.pth."
        "\n\n"
        "Метрики экспозиции и резкости реализованы классическими алгоритмами без обучения: пороги задаются "
        "пользователем в настройках приложения."
    )
    doc.add_paragraph(arch)

    # --- 6 UI ---
    add_heading("6", "Интеграция и пользовательский интерфейс")
    ui = (
        "Пользовательский интерфейс реализован на стандартной библиотеке Tkinter (модули tkinter, ttk)."
        "\n\n"
        "Раздел «Анализ»: выбор рабочей папки с фотографиями, запуск и остановка пакетной обработки, "
        "прогресс и журнал сообщений, переход к папкам «Удачные» и «Неудачные» на диске."
        "\n\n"
        "Раздел «Альбомы»: просмотр миниатюр и превью, ручной перенос выбранных файлов между альбомами."
        "\n\n"
        "Раздел «Настройки»: пороги недосвета и пересвета, доля экстремальных пикселей, порог резкости, "
        "включение дополнительной проверки глаз ResNet. Параметры сохраняются в app_settings.json."
        "\n\n"
        "Модуль src/analysis/pipeline.py последовательно вызывает проверку глаз, экспозиции и резкости и "
        "формирует итоговое заключение по каждому файлу."
    )
    doc.add_paragraph(ui)

    # --- 7 Заключение ---
    add_heading("7", "Заключение")
    conc = (
        "В ходе проекта разработано десктоп-приложение для автоматической сортировки фотографий по качеству "
        "экспозиции, резкости и признаку закрытых глаз. Реализованы классические методы анализа изображения "
        "и современные средства детекции лица MediaPipe, обучен свёрточный классификатор на датасете MRL."
        "\n\n"
        "Практическая ценность заключается в сокращении времени отбора кадров перед дальнейшей обработкой "
        "или печатью. Направления развития: расширение критериев отбора, оптимизация скорости на слабых ПК, "
        "улучшение устойчивости к профилю лица и сложному освещению."
    )
    doc.add_paragraph(conc)

    # --- 8 Источники ---
    add_heading("8", "Список использованных источников")
    for i, src in enumerate(
        [
            "Документация MediaPipe Face Landmarker. URL: https://ai.google.dev/edge/mediapipe/solutions/vision/face_landmarker",
            "MRL Eye Dataset. URL: https://mrl.cs.vsb.cz/eyedataset",
            "Документация PyTorch. URL: https://pytorch.org/docs/stable/index.html",
            "Документация OpenCV. URL: https://docs.opencv.org/",
            "Документация Python Tkinter. URL: https://docs.python.org/3/library/tkinter.html",
        ],
        start=1,
    ):
        doc.add_paragraph(f"{i}. {src}")

    doc.save(out)
    print(f"Сохранено: {out}")


if __name__ == "__main__":
    main()
